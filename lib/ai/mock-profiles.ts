export function getAnalysisProfile(value: string): AnalysisProfile {
  const normalized = value.toLocaleLowerCase("ko-KR");

  if (
    normalized.includes("66north") ||
    normalized.includes("66°north") ||
    normalized.includes("아웃도어")
  ) {
    return north66Profile;
  }

  if (
    normalized.includes("오딧세이") ||
    normalized.includes("스킨케어") ||
    normalized.includes("광고")
  ) {
    return odysseyProfile;
  }

  if (
    normalized.includes("biscuit") ||
    normalized.includes("카드뉴스") ||
    normalized.includes("콘텐츠")
  ) {
    return biscuitProfile;
  }

  if (
    normalized.includes("자기소개서") ||
    normalized.includes("자소서")
  ) {
    return coverLetterProfile;
  }

  return defaultProfile;
}
